#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		*(char *)b = c;
		b = (char *)b + 1;
		i++;
	}
	return ((char *)b - i);
}
